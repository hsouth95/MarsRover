namespace MarsRover.Enums
{
    /// <summary>
    /// An enumeration of all the possible actions
    /// </summary>
    public enum Action
    {
        /// <summary>
        /// Represents an action of moving forward
        /// </summary>
        MoveForward,

        /// <summary>
        /// Represents an action of rotating left
        /// </summary>
        RotateLeft,

        /// <summary>
        /// Represents an action of rotating right
        /// </summary>
        RotateRight,

        /// <summary>
        /// Represents an empty action
        /// </summary>
        Nothing
    }
}
